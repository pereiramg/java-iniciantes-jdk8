/*
    Demostra um bloco de codigo
*/

public class BlockDemo {
    
    public static void main(String args[]){

        // definição das variaveis
        double i, j,d;

        i = 5;
        j = 10;
        if (i != 0){
            System.out.println("i não é igual a zero");
            d = j / i;
            System.out.println("j / i é " + d);
        }
    }
}